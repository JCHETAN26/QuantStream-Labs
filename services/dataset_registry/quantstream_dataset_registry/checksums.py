"""SHA-256 manifest handling for the demo dataset.

The dataset ships a ``SHA256SUMS`` file in the standard ``sha256sum`` format:

    <64-hex-digest>  <filename>

Two spaces separate the digest from the filename (the coreutils convention). We
verify every listed file and fail loudly on any mismatch or missing file — a silent
checksum skip would defeat the entire point of a reproducibility dataset.
"""

from __future__ import annotations

import hashlib
from dataclasses import dataclass
from pathlib import Path

SHA256SUMS_NAME = "SHA256SUMS"
_CHUNK = 1 << 20  # 1 MiB


def sha256_file(path: Path) -> str:
    """Streaming SHA-256 hex digest of a file."""
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        while chunk := handle.read(_CHUNK):
            digest.update(chunk)
    return digest.hexdigest()


def sha256_bytes(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


def render_sha256sums(entries: dict[str, str]) -> str:
    """Render a SHA256SUMS body from {filename: digest}, sorted by filename.

    Sorting makes the file itself deterministic so it can be committed and
    regenerated byte-for-byte.
    """
    lines = [f"{entries[name]}  {name}" for name in sorted(entries)]
    return "\n".join(lines) + "\n"


def parse_sha256sums(text: str) -> dict[str, str]:
    """Parse a SHA256SUMS body into {filename: digest}."""
    result: dict[str, str] = {}
    for raw in text.splitlines():
        line = raw.strip()
        if not line or line.startswith("#"):
            continue
        # Format: "<digest>  <name>" (two spaces, coreutils style). Split on the
        # first run of whitespace so filenames with spaces still work.
        digest, _, name = line.partition(" ")
        name = name.strip()
        if not digest or not name:
            raise ValueError(f"malformed SHA256SUMS line: {raw!r}")
        result[name] = digest.lower()
    return result


@dataclass(frozen=True)
class VerifyResult:
    ok: bool
    verified: tuple[str, ...]
    mismatched: tuple[str, ...]
    missing: tuple[str, ...]

    @property
    def total(self) -> int:
        return len(self.verified) + len(self.mismatched) + len(self.missing)

    def summary(self) -> str:
        return f"{len(self.verified)}/{self.total}"


class ChecksumError(RuntimeError):
    """Raised when the dataset fails checksum verification."""


def verify_directory(directory: Path, sums: dict[str, str]) -> VerifyResult:
    """Verify every file listed in ``sums`` against files in ``directory``."""
    verified: list[str] = []
    mismatched: list[str] = []
    missing: list[str] = []

    for name in sorted(sums):
        path = directory / name
        if not path.is_file():
            missing.append(name)
            continue
        if sha256_file(path) == sums[name].lower():
            verified.append(name)
        else:
            mismatched.append(name)

    return VerifyResult(
        ok=not mismatched and not missing,
        verified=tuple(verified),
        mismatched=tuple(mismatched),
        missing=tuple(missing),
    )


def verify_or_raise(directory: Path, sums: dict[str, str]) -> VerifyResult:
    """Verify and raise ChecksumError with a precise message on any failure."""
    result = verify_directory(directory, sums)
    if not result.ok:
        problems = []
        if result.mismatched:
            problems.append("mismatched: " + ", ".join(result.mismatched))
        if result.missing:
            problems.append("missing: " + ", ".join(result.missing))
        raise ChecksumError(
            f"dataset checksum verification failed ({'; '.join(problems)})"
        )
    return result
